// Crie a soma de dois números e mostre a soma entre eles
Console.WriteLine("Script de soma de dois números");
Console.WriteLine("Informa o primeiro número:");
int n1 = int.Parse(Console.ReadLine());
Console.WriteLine("Informe a segundo número: ");
int n2 = int.Parse(Console.ReadLine());
int soma = n1 + n2;
Console.Write("A soma entre " + n1+"+"+n2+"="+soma);

